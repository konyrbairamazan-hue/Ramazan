#Tuple
thistuple = ("apple", "banana", "cherry")
print(thistuple)


#Allow Duplicates
thistuple = ("apple", "banana", "cherry", "apple", "cherry")
print(thistuple)


#Tuple Length
thistuple = ("apple", "banana", "cherry")
print(len(thistuple))


#Loop Through a Tuple
thistuple = ("apple", "banana", "cherry")
for x in thistuple:
  print(x)


