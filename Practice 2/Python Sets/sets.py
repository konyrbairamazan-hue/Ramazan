#Set
thisset = {"apple", "banana", "cherry"}
print(thisset)


#Get the Length of a Set
thisset = {"apple", "banana", "cherry"}

print(len(thisset))


#Duplicates Not Allowed
thisset = {"apple", "banana", "cherry", "apple"}

print(thisset)


#Add Items
thisset = {"apple", "banana", "cherry"}

thisset.add("orange")

print(thisset)


#Remove Item
thisset = {"apple", "banana", "cherry"}
thisset.remove("banana")
print(thisset)